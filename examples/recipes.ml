правило містить_тваринне(?с) якщо інгредієнт(?с, ?і) і тваринне(?і).
правило містить_тваринне(?с) якщо містить_м_ясо(?с).
правило підходить_вегетаріанцю(?с) якщо страва(?с) і не містить_м_ясо(?с).
правило веганська(?с) якщо страва(?с) і не містить_тваринне(?с).

