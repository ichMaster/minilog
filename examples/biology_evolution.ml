% для нових сутностей в базі знань
%
% Використання:
%   .venv/bin/minilog repl examples/biology_evolution.ml
%   minilog> :evolve 10
%
% Базова біологія (підмножина biology.ml)

% Таксономія
є(горобець, птах).
є(орел, птах).
є(пінгвін, птах).
є(птах, тварина).
є(кіт, ссавець).
є(ссавець, тварина).
є(тварина, живе).
є(лосось, риба).
є(риба, тварина).

% Властивості
має(птах, крила).
має(птах, дзьоб).
має(ссавець, хутро).
має(риба, плавці).
має(живе, клітини).

% Виключення
не_літає(пінгвін).

% Правила — успадкування
правило успадковує(?x, ?p) якщо має(?x, ?p).
правило успадковує(?x, ?p) якщо є(?x, ?y) і успадковує(?y, ?p).

% Правило — літає якщо птах і не виключено
правило літає(?x) якщо є(?x, птах) і не не_літає(?x).

% Базові запити (до еволюції)
?- літає(?x).
?- успадковує(горобець, крила).
