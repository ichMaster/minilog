% ============================================================
% school_schedule.ml — планування шкільного розкладу
% ============================================================
%
% Класична задача constraint satisfaction: призначити уроки
% на часові слоти так, щоб виконувалися всі обмеження
% (жоден клас не має двох уроків одночасно, жоден вчитель не
% веде двох уроків одночасно, кабінети не перекриваються).
%
% Це третій приклад combinatorial search у minilog, після
% graph_coloring.ml і hamiltonian_path.ml. Усі три демонструють
% ту саму ідіому generate-and-test з incremental constraint
% satisfaction, але на різних предметних областях:
%   - розфарбування графа: теорія графів
%   - гамільтонів шлях: обхід графа
%   - розклад: реалістична планувальна задача
%
% Сценарій навмисно малий, щоб і сам файл, і expected output
% були керованими. Реальний шкільний розклад на 500 уроків
% minilog не вирішить — для цього треба ASP (Clingo) або
% CLP(FD) у SWI-Prolog, де constraint propagation робить цю
% задачу практично розв'язуваною за секунди.
%
% Сценарій цього прикладу:
%   - 2 класи (клас_а, клас_б)
%   - 3 часові слоти (понеділок_урок1, понеділок_урок2, вівторок_урок1)
%   - 3 вчителі (іваненко — математика, петренко — читання,
%     сидоренко — фізкультура)
%   - 4 уроки: клас_а математика, клас_а читання, клас_б математика,
%     клас_б фізкультура
%   - 2 кабінети: кімната (для математики і читання),
%     спортзал (для фізкультури)
%
% Обмеження:
%   H1. Клас клас_а не має двох уроків одночасно: ?m1 ≠ ?r1
%   H2. Клас клас_б не має двох уроків одночасно: ?m2 ≠ ?f2
%   H3. Вчитель іваненко не веде два уроки одночасно: ?m1 ≠ ?m2
%   H4. Кімната не приймає два класи одночасно: ?r1 ≠ ?m2
%
% Вчителі петренко і сидоренко ведуть лише по одному уроку,
% тому для них обмежень не треба. Спортзал використовує
% тільки один урок фізкультури, тому теж без обмежень.
% ============================================================


% ------------------------------------------------------------
% Ресурси
% ------------------------------------------------------------

% Класи
клас(клас_а).
клас(клас_б).

% Часові слоти
слот(понеділок_урок1).
слот(понеділок_урок2).
слот(вівторок_урок1).

% Вчителі і їхні предмети
вчитель(іваненко, математика).
вчитель(петренко, читання).
вчитель(сидоренко, фізкультура).

% Кабінети
кабінет(кімната).
кабінет(спортзал).

% Відповідність предмета і кабінету
кабінет_предмета(математика, кімната).
кабінет_предмета(читання, кімната).
кабінет_предмета(фізкультура, спортзал).


% Оператор ≠ працює на будь-яких термах (атоми, числа, рядки),
% тому окремі факти різності не потрібні.


% ============================================================
% Правило планування розкладу
% ============================================================
%
% Параметри розкладу:
%   ?m1 — слот уроку математики класу клас_а
%   ?r1 — слот уроку читання класу клас_а
%   ?m2 — слот уроку математики класу клас_б
%   ?f2 — слот уроку фізкультури класу клас_б
%
% Incremental constraint satisfaction: обмеження перевіряються
% одразу, щойно з'являються зв'язані змінні. Це класичний
% прологівський патерн "fail fast" — невалідні гілки
% обрізаються якомога раніше, до того як movemok встигне
% витратити час на безнадійну комбінацію.
% ============================================================

правило розклад(?m1, ?r1, ?m2, ?f2) якщо слот(?m1) і слот(?r1) і ?m1 ≠ ?r1 і слот(?m2) і ?m1 ≠ ?m2 і ?r1 ≠ ?m2 і слот(?f2) і ?m2 ≠ ?f2.


% ============================================================
% Запити
% ============================================================

% Знайти всі валідні розклади. Очікувано 12 розв'язків:
% для кожної з 6 перестановок (m1, r1, m2) є 2 варіанти f2.
?- розклад(?m1, ?r1, ?m2, ?f2).

% Конкретна перевірка: один з валідних розкладів
% m1=пн_урок1, r1=пн_урок2, m2=вт_урок1, f2=пн_урок1
?- розклад(понеділок_урок1, понеділок_урок2, вівторок_урок1, понеділок_урок1).

% Негативний приклад: m1 і r1 у тому самому слоті — порушує H1
?- розклад(понеділок_урок1, понеділок_урок1, вівторок_урок1, понеділок_урок2).

% Traced запит: показати як movemok шукає розклад
?- слід розклад(?m1, ?r1, ?m2, ?f2).


% ============================================================
% Примітка про масштабованість
% ============================================================
%
% Цей приклад має 3 слоти і 4 уроки. Простір пошуку:
%   3^4 = 81 комбінація до фільтрування.
% З incremental constraints backtracking відкидає невалідні
% гілки рано, залишаючи 12 валідних розв'язків.
%
% Для реального шкільного розкладу:
%   - 30 класів × 20 уроків/тиждень = 600 уроків
%   - 50 вчителів
%   - 40 часових слотів на тиждень
%   - 30 кабінетів
% Простір пошуку наївний: 40^600 — астрономічне число.
%
% Такий масштаб вирішується:
%   - ASP (Clingo) через декларативні обмеження з built-in
%     constraint propagation. ~20 рядків коду, секунди
%     виконання
%   - CLP(FD) у SWI-Prolog через domain reasoning і propagators
%   - OR-Tools (Google) для індустріальних задач
%   - Спеціалізовані timetabling solvers (FET, aSc Timetables)
%
% minilog без constraint propagation виконує чистий
% backtracking, який експоненційний у найгіршому випадку.
% Для навчання ідіоми — ідеально. Для серйозного розкладу —
% потрібен інший інструмент.
%
% Також зверніть увагу, що правило розклад/4 має ФІКСОВАНУ
% арність. Для 6 уроків потрібне було б розклад/6, для 10 —
% розклад/10. Генеричне "заплануй N уроків" у minilog Phase 1
% неможливе без списків як first-class.
% ============================================================
