% ------------------------------------------------------------
% Продукційні правила
% ------------------------------------------------------------

% Старіння: кожне покоління додає 10 років
продукція старіння якщо особа(?x, ?v) і живий(?x) додати особа(?x, ?v_новий) видалити особа(?x, ?v) коли ?v_новий = ?v + 10.

% Повноліття: дитина → дорослий при віці ≥ 18
продукція повноліття якщо дитина(?x) і особа(?x, ?v) додати дорослий(?x) видалити дитина(?x) коли ?v ≥ 18.

% Літність: дорослий → літній при віці ≥ 60
продукція літність якщо дорослий(?x) і особа(?x, ?v) додати літній(?x) видалити дорослий(?x) коли ?v ≥ 60.

% Смерть: літній із віком > 90 помирає
продукція смерть якщо літній(?x) і особа(?x, ?v) і живий(?x) видалити живий(?x), літній(?x), особа(?x, ?v) коли ?v > 90.

% Очистка: видалити записи особа для тих, хто вже не живий
% (потрібно, бо старіння і смерть в одному поколінні можуть
% залишити phantom-запис особа після видалення живий)
продукція очистка якщо особа(?x, ?v) і не живий(?x) видалити особа(?x, ?v).


