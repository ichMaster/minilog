% ------------------------------------------------------------
% Точки на площині (назва, x, y)
% ------------------------------------------------------------

точка(a, 0, 0).
точка(b, 4, 0).
точка(c, 0, 3).
точка(d, 2, 2).
точка(e, 6, 0).
точка(f, 3, 5).
точка(g, 8, 0).
точка(h, 4, 4).


% ------------------------------------------------------------
% Трикутники (назва, точка1, точка2, точка3)
% ------------------------------------------------------------

% t1: прямокутний 3-4-5 (a-b-c)
трикутник(t1, a, b, c).

% t2: рівнобедрений (a-d-e з a=(0,0) d=(2,2) e=(6,0))
трикутник(t2, a, d, e).

% t3: різносторонній (a-f-e)
трикутник(t3, a, f, e).

% t4: рівносторонній-подібний (координати підібрані)
трикутник(t4, a, b, h).

% t5: вироджений (колінеарний: a-b-g всі на осі x)
трикутник(t5, a, b, g).


% ------------------------------------------------------------
% Кути трикутників (трикутник, вершина, градуси)
% ------------------------------------------------------------

кут(t1, a, 90).
кут(t1, b, 37).
кут(t1, c, 53).

кут(t2, a, 72).
кут(t2, d, 72).
кут(t2, e, 36).

кут(t3, a, 40).
кут(t3, f, 65).
кут(t3, e, 75).

кут(t4, a, 45).
кут(t4, b, 90).
кут(t4, h, 45).

кут(t5, a, 0).
кут(t5, b, 0).
кут(t5, g, 180).


% ------------------------------------------------------------
% Правила: обчислення відстані
% ------------------------------------------------------------

правило відстань(?p1, ?p2, ?d) якщо точка(?p1, ?x1, ?y1) і точка(?p2, ?x2, ?y2) і ?p1 != ?p2 і ?d = sqrt((?x2 - ?x1) * (?x2 - ?x1) + (?y2 - ?y1) * (?y2 - ?y1)).

правило сторони(?t, ?ab, ?bc, ?ac) якщо трикутник(?t, ?a, ?b, ?c) і відстань(?a, ?b, ?ab) і відстань(?b, ?c, ?bc) і відстань(?a, ?c, ?ac).


% ------------------------------------------------------------
% Класифікація за сторонами
% ------------------------------------------------------------

правило рівносторонній(?t) якщо сторони(?t, ?a, ?b, ?c) і ?a = ?b і ?b = ?c.
правило рівнобедрений(?t) якщо сторони(?t, ?a, ?b, ?c) і ?a = ?b.
правило рівнобедрений(?t) якщо сторони(?t, ?a, ?b, ?c) і ?b = ?c.
правило рівнобедрений(?t) якщо сторони(?t, ?a, ?b, ?c) і ?a = ?c.


% ------------------------------------------------------------
% Класифікація за кутами
% ------------------------------------------------------------

правило прямокутний(?t) якщо кут(?t, ?v, 90).
правило тупокутний(?t) якщо кут(?t, ?v, ?a) і ?a > 90.
правило гострокутний(?t) якщо трикутник(?t, ?p1, ?p2, ?p3) і не прямокутний(?t) і не тупокутний(?t).


% ------------------------------------------------------------
% Нерівність трикутника і теорема Піфагора
% ------------------------------------------------------------

правило валідний_трикутник(?t) якщо сторони(?t, ?a, ?b, ?c) і ?a + ?b > ?c і ?b + ?c > ?a і ?a + ?c > ?b.

правило піфагоровий(?t) якщо сторони(?t, ?a, ?b, ?c) і ?a * ?a + ?b * ?b = ?c * ?c.
правило піфагоровий(?t) якщо сторони(?t, ?a, ?b, ?c) і ?a * ?a + ?c * ?c = ?b * ?b.
правило піфагоровий(?t) якщо сторони(?t, ?a, ?b, ?c) і ?b * ?b + ?c * ?c = ?a * ?a.


