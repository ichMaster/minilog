% ============================================================
% calculator.ml — AST walker: обчислення арифметичних виразів
% ============================================================
%
% Класичний приклад логічного програмування: обчислення значення
% дерева розбору (AST) арифметичного виразу через структурну
% рекурсію на складених термах.
%
% Дерево розбору задається як факти (minilog не має парсера
% виразів із рядків). Правила обчислити/2 рекурсивно обходять
% дерево і обчислюють значення у листках.
%
% Три типи вузлів:
%   число(N)            — листок із числовою константою
%   бін(Op, L, R)       — бінарний оператор (плюс, мінус, множення, ділення)
%   функція(Name, Arg)  — унарна функція (sqrt, abs)
%   функція_pow(B, E)   — степінь (pow)
%
% Див. docs/calculator-explained.md для детального пояснення.
% Вимагає Phase 9 арифметику (MINILOG-021..024, MINILOG-061).
% ============================================================


% ------------------------------------------------------------
% Оператори
% ------------------------------------------------------------

оператор(плюс).
оператор(мінус).
оператор(множення).
оператор(ділення).


% ------------------------------------------------------------
% Правила обчислення (одне правило на форму вузла)
% ------------------------------------------------------------

% Листок: значення числа — саме число
правило обчислити(число(?n), ?v) якщо ?v = ?n.

% Бінарні оператори
правило обчислити(бін(плюс, ?l, ?r), ?v) якщо обчислити(?l, ?lv) і обчислити(?r, ?rv) і ?v = ?lv + ?rv.
правило обчислити(бін(мінус, ?l, ?r), ?v) якщо обчислити(?l, ?lv) і обчислити(?r, ?rv) і ?v = ?lv - ?rv.
правило обчислити(бін(множення, ?l, ?r), ?v) якщо обчислити(?l, ?lv) і обчислити(?r, ?rv) і ?v = ?lv * ?rv.
правило обчислити(бін(ділення, ?l, ?r), ?v) якщо обчислити(?l, ?lv) і обчислити(?r, ?rv) і ?v = ?lv / ?rv.

% Унарні функції
правило обчислити(функція(sqrt, ?a), ?v) якщо обчислити(?a, ?av) і ?v = sqrt(?av).
правило обчислити(функція(abs, ?a), ?v) якщо обчислити(?a, ?av) і ?v = abs(?av).

% Степінь (двоаргументна функція)
правило обчислити(функція_pow(?base, ?exp), ?v) якщо обчислити(?base, ?bv) і обчислити(?exp, ?ev) і ?v = pow(?bv, ?ev).


% ------------------------------------------------------------
% Структурні предикати (не обчислюють, лише перевіряють форму)
% ------------------------------------------------------------

% є_листком: перевіряється через запит вираз(?name, число(?_))
% бінарний_вузол: перевіряється через запит вираз(?name, бін(?_, ?_, ?_))


% ------------------------------------------------------------
% Іменовані приклади дерев
% ------------------------------------------------------------

% ex1: проста константа → 42
вираз(ex1, число(42)).

% ex2: 1 + 2 → 3
вираз(ex2, бін(плюс, число(1), число(2))).

% ex3: 1 + 2 * 3 → 7 (пріоритет закодовано в дереві)
вираз(ex3, бін(плюс, число(1), бін(множення, число(2), число(3)))).

% ex4: (10 + 5) - 3 → 12
вираз(ex4, бін(мінус, бін(плюс, число(10), число(5)), число(3))).

% ex5: 10 / 4 → 2.5
вираз(ex5, бін(ділення, число(10), число(4))).

% ex6: sqrt(3^2 + 4^2) → 5.0 (піфагорова відстань)
вираз(ex6, функція(sqrt, бін(плюс, функція_pow(число(3), число(2)), функція_pow(число(4), число(2))))).

% ex7: abs(3 - 10) → 7
вираз(ex7, функція(abs, бін(мінус, число(3), число(10)))).

% ex8: 1 / 0 → помилка ділення на нуль
вираз(ex8, бін(ділення, число(1), число(0))).


% ============================================================
% Запити
% ============================================================

% Допоміжне правило для запитів
правило обчислити_вираз(?name, ?v) якщо вираз(?name, ?t) і обчислити(?t, ?v).

% Обчислення кожного прикладу (ex1..ex7)
?- обчислити_вираз(ex1, ?v).
?- обчислити_вираз(ex2, ?v).
?- обчислити_вираз(ex3, ?v).
?- обчислити_вираз(ex4, ?v).
?- обчислити_вираз(ex5, ?v).
?- обчислити_вираз(ex6, ?v).
?- обчислити_вираз(ex7, ?v).

% ex8: ділення на нуль — очікується EvaluatorError
% ?- обчислити_вираз(ex8, ?v).

% Структурні запити (не обчислюють, лише pattern matching)
?- вираз(?name, число(?n)).
?- вираз(?name, бін(?op, ?l, ?r)).

% Трасований запит: proof tree для 1 + 2
?- слід обчислити(бін(плюс, число(1), число(2)), ?v).


