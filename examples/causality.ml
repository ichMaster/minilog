% causality.ml — причинно-наслідкові зв'язки
% Демонструє: транзитивна каузація, знаходження спільної причини

% Прямі причинно-наслідкові зв'язки
причина(дощ, мокра_дорога).
причина(мокра_дорога, ковзанка).
причина(ковзанка, аварія).
причина(туман, погана_видимість).
причина(погана_видимість, аварія).
причина(мороз, ожеледиця).
причина(ожеледиця, ковзанка).
причина(дощ, повінь).
причина(повінь, затоплення).
причина(вітер, повалене_дерево).
причина(повалене_дерево, відключення_електрики).
причина(блискавка, пожежа).
причина(спека, посуха).
причина(посуха, неврожай).

% Правила
правило призводить_до(?x, ?y) якщо причина(?x, ?y).
правило призводить_до(?x, ?z) якщо причина(?x, ?y) і призводить_до(?y, ?z).

правило спільна_причина(?e1, ?e2, ?c) якщо призводить_до(?c, ?e1) і призводить_до(?c, ?e2).

% Запити
?- призводить_до(дощ, ?x).
?- спільна_причина(мокра_дорога, повінь, ?c).
