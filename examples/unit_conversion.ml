% ============================================================
% unit_conversion.ml — конвертація одиниць та фізичні формули
% ============================================================
% Демонструє практичне використання арифметики: конвертація
% температури, відстані, ваги; обчислення BMI, швидкості,
% енергії. Кожне правило — одна формула з шкільної фізики.
%
% Вимагає Phase 9 арифметику.
% ============================================================


% ------------------------------------------------------------
% Температура: дані в Цельсіях
% ------------------------------------------------------------

температура(київ, 22).
температура(лондон, 15).
температура(нью_йорк, 30).
температура(рейк_явік, -2).
температура(каїр, 38).
температура(токіо, 26).

% Цельсій → Фаренгейт: F = C * 9/5 + 32
правило фаренгейт(?місто, ?f) якщо температура(?місто, ?c) і ?f = ?c * 9 / 5 + 32.

% Цельсій → Кельвін: K = C + 273
правило кельвін(?місто, ?k) якщо температура(?місто, ?c) і ?k = ?c + 273.

% Класифікація
правило спекотно(?місто) якщо температура(?місто, ?c) і ?c > 35.
правило холодно(?місто) якщо температура(?місто, ?c) і ?c < 5.
правило комфортно(?місто) якщо температура(?місто, ?c) і ?c ≥ 18 і ?c ≤ 25.


% ------------------------------------------------------------
% Відстань між містами (км)
% ------------------------------------------------------------

відстань_км(київ, львів, 540).
відстань_км(київ, одеса, 475).
відстань_км(київ, харків, 480).
відстань_км(львів, одеса, 800).

% км → милі: 1 км = 0.621371 милі
правило відстань_милі(?a, ?b, ?m) якщо відстань_км(?a, ?b, ?км) і ?m = ?км * 621371 / 1000000.

% Час подорожі (години) при заданій середній швидкості
правило час_подорожі(?a, ?b, ?швидкість, ?час) якщо відстань_км(?a, ?b, ?км) і ?швидкість > 0 і ?час = ?км / ?швидкість.


% ------------------------------------------------------------
% Люди: зріст (см), вага (кг)
% ------------------------------------------------------------

людина(олена, 165, 58).
людина(андрій, 182, 85).
людина(марія, 170, 62).
людина(петро, 175, 110).
людина(софія, 160, 50).

% BMI = вага / (зріст_м ^ 2), де зріст_м = зріст_см / 100
правило бмі(?ім_я, ?bmi) якщо людина(?ім_я, ?зріст, ?вага) і ?bmi = ?вага / ((?зріст / 100) * (?зріст / 100)).

% Класифікація BMI
правило недостатня_вага(?ім_я) якщо бмі(?ім_я, ?b) і ?b < 18.5.
правило нормальна_вага(?ім_я) якщо бмі(?ім_я, ?b) і ?b ≥ 18.5 і ?b < 25.
правило надмірна_вага(?ім_я) якщо бмі(?ім_я, ?b) і ?b ≥ 25 і ?b < 30.
правило ожиріння(?ім_я) якщо бмі(?ім_я, ?b) і ?b ≥ 30.


% ------------------------------------------------------------
% Фізика: кінетична енергія
% ------------------------------------------------------------

об_єкт(м_яч, 0.45).
об_єкт(автомобіль, 1500).
об_єкт(велосипед, 15).

швидкість(м_яч, 30).
швидкість(автомобіль, 25).
швидкість(велосипед, 8).

% E = m * v^2 / 2
правило кінетична_енергія(?назва, ?e) якщо об_єкт(?назва, ?m) і швидкість(?назва, ?v) і ?e = ?m * ?v * ?v / 2.


