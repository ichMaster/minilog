% Точки на площині
точка(a, 0, 0).
точка(b, 3, 0).
точка(c, 0, 4).
точка(d, 6, 8).
точка(e, 1, 1).
точка(f, 4, 5).

% Правила

% Перевірка теореми Піфагора
правило піфагорова_трійка(?a, ?b, ?c) якщо ?a * ?a + ?b * ?b = ?c * ?c.

% Відстань між точками менше порогу
правило близькі(?p1, ?p2, ?поріг) якщо точка(?p1, ?x1, ?y1) і точка(?p2, ?x2, ?y2) і ?p1 != ?p2 і sqrt((?x2 - ?x1) * (?x2 - ?x1) + (?y2 - ?y1) * (?y2 - ?y1)) < ?поріг.

% Квадрант точки
правило перший_квадрант(?p) якщо точка(?p, ?x, ?y) і ?x > 0 і ?y > 0.

% Нерівність трикутника
правило валідний_трикутник(?a, ?b, ?c) якщо ?a + ?b > ?c і ?b + ?c > ?a і ?a + ?c > ?b.

% Середня точка
правило середина_x(?p1, ?p2, ?mx) якщо точка(?p1, ?x1, ?_) і точка(?p2, ?x2, ?_) і ?mx = (?x1 + ?x2) / 2.

правило відстань(?p1, ?p2, ?dist) якщо точка(?p1, ?x1, ?y1) і точка(?p2, ?x2, ?y2) і ?p1 != ?p2 і ?dist = sqrt((?x2 - ?x1) * (?x2 - ?x1) + (?y2 - ?y1) * (?y2 - ?y1)). 
правило трикутник_гострокутний(?p1, ?p2, ?p3) якщо відстань(?p1, ?p2, ?a) і відстань(?p1, ?p3, ?b) і відстань(?p2, ?p3, ?c) і pow(?a, 2) + pow(?b, 2) > pow(?c, 2).
правило трикутник_тупокутний(?p1, ?p2, ?p3) якщо відстань(?p1, ?p2, ?a) і відстань(?p1, ?p3, ?b) і відстань(?p2, ?p3, ?c) і pow(?a, 2) + pow(?b, 2) < pow(?c, 2).

правило трикутник_прямокутний(?p1, ?p2, ?p3) якщо відстань(?p1, ?p2, ?a) і відстань(?p1, ?p3, ?b) і відстань(?p2, ?p3, ?c) і pow(?a, 2) + pow(?b, 2) = pow(?c, 2).
правило трикутник_прямокутний(?p1, ?p2, ?p3) якщо відстань(?p1, ?p2, ?a) і відстань(?p1, ?p3, ?b) і відстань(?p2, ?p3, ?c) і pow(?a, 2) + pow(?c, 2) = pow(?b, 2).
правило трикутник_прямокутний(?p1, ?p2, ?p3) якщо відстань(?p1, ?p2, ?a) і відстань(?p1, ?p3, ?b) і відстань(?p2, ?p3, ?c) і pow(?b, 2) + pow(?c, 2) = pow(?a, 2).


