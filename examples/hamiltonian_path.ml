% ============================================================
% hamiltonian_path.ml — пошук гамільтонових шляхів і циклів
% ============================================================
%
% Гамільтонів шлях у графі — це послідовність вершин, у якій
% кожна вершина графа з'являється РІВНО ОДИН РАЗ, і кожна пара
% послідовних вершин з'єднана ребром. Якщо додатково перша і
% остання вершини з'єднані — це гамільтонів цикл.
%
% Задача пошуку гамільтонового шляху у довільному графі —
% NP-повна. У чистому Prolog вона вирішується одним правилом
% через generate-and-test: перебрати усі впорядковані набори
% різних вершин і відфільтрувати ті, де всі послідовні пари
% є ребрами. Backtracking виконує комбінаторний пошук
% автоматично.
%
% У minilog є одне обмеження, яке впливає на цей приклад:
%
% Немає списків як first-class, тому правило має ФІКСОВАНУ
%    арність. Для графа на 4 вершини — правило /4, на 5 вершин —
%    правило /5. Генеричне "гам_шлях(?g, ?path)" у minilog
%    Phase 1 неможливе.
%
% Три графи у цьому файлі демонструють різну структуру:
%   g1: квадрат C4 (4 вершини, 4 ребра) — 8 шляхів, 8 циклів
%   g2: повний K4 (4 вершини, 6 ребер) — 24 шляхи, 24 цикли
%   g3: лінійний P5 (5 вершин, 4 ребра) — 2 шляхи, 0 циклів
%
% Для реальних задач пошуку гамільтонового шляху на великих
% графах використовують ASP (Clingo) або спеціалізовані
% SAT-solver-и — вони вирішують сотні вершин за секунди.
% minilog тут — для навчання класичної прологівської ідіоми.
% ============================================================


% Оператор ≠ працює на будь-яких термах, тому окремі факти різності не потрібні.


% ============================================================
% Граф 1: квадрат C4
% ============================================================
%
%     v1 --- v2
%     |       |
%     |       |
%     v4 --- v3
%
% 4 вершини, 4 ребра. Кожна вершина має рівно 2 сусіди.
% Хроматичне число 2, граф двочастковий, гамільтонів.
% ------------------------------------------------------------

вершина_g1(v1).
вершина_g1(v2).
вершина_g1(v3).
вершина_g1(v4).

% Ребра в обидва напрямки (4 × 2 = 8 фактів)
ребро_g1(v1, v2).  ребро_g1(v2, v1).
ребро_g1(v2, v3).  ребро_g1(v3, v2).
ребро_g1(v3, v4).  ребро_g1(v4, v3).
ребро_g1(v4, v1).  ребро_g1(v1, v4).

% Правило: generate-and-test з incremental constraint satisfaction.
% Обмеження різності і ребер перевіряються одразу, щойно
% з'являються зв'язані змінні — це стискає пошуковий простір.
правило гам_шлях_g1(?a, ?b, ?c, ?d) якщо вершина_g1(?a) і вершина_g1(?b) і ?a ≠ ?b і ребро_g1(?a, ?b) і вершина_g1(?c) і ?a ≠ ?c і ?b ≠ ?c і ребро_g1(?b, ?c) і вершина_g1(?d) і ?a ≠ ?d і ?b ≠ ?d і ?c ≠ ?d і ребро_g1(?c, ?d).

% Гамільтонів цикл: шлях плюс ребро від останньої вершини до першої
правило гам_цикл_g1(?a, ?b, ?c, ?d) якщо гам_шлях_g1(?a, ?b, ?c, ?d) і ребро_g1(?d, ?a).


% ============================================================
% Граф 2: повний граф K4
% ============================================================
%
%     v1 --- v2
%     |\\   /|
%     | \\ / |
%     |  X  |
%     | / \\ |
%     |/   \\|
%     v4 --- v3
%
% 4 вершини, 6 ребер (усі пари з'єднані). Оскільки граф повний,
% будь-яка перестановка вершин — валідний шлях. Очікувана
% кількість розв'язків: 4! = 24.
% ------------------------------------------------------------

вершина_g2(v1).
вершина_g2(v2).
вершина_g2(v3).
вершина_g2(v4).

% Усі 6 ребер в обидва напрямки (12 фактів)
ребро_g2(v1, v2).  ребро_g2(v2, v1).
ребро_g2(v1, v3).  ребро_g2(v3, v1).
ребро_g2(v1, v4).  ребро_g2(v4, v1).
ребро_g2(v2, v3).  ребро_g2(v3, v2).
ребро_g2(v2, v4).  ребро_g2(v4, v2).
ребро_g2(v3, v4).  ребро_g2(v4, v3).

правило гам_шлях_g2(?a, ?b, ?c, ?d) якщо вершина_g2(?a) і вершина_g2(?b) і ?a ≠ ?b і ребро_g2(?a, ?b) і вершина_g2(?c) і ?a ≠ ?c і ?b ≠ ?c і ребро_g2(?b, ?c) і вершина_g2(?d) і ?a ≠ ?d і ?b ≠ ?d і ?c ≠ ?d і ребро_g2(?c, ?d).

правило гам_цикл_g2(?a, ?b, ?c, ?d) якщо гам_шлях_g2(?a, ?b, ?c, ?d) і ребро_g2(?d, ?a).


% ============================================================
% Граф 3: лінійний шлях P5
% ============================================================
%
%     v1 --- v2 --- v3 --- v4 --- v5
%
% 5 вершин, 4 ребра. Це дерево-шлях. Гамільтонових шляхів
% рівно 2 (обхід зліва направо і навпаки). Гамільтонових
% циклів немає, бо v1 і v5 не з'єднані.
% ------------------------------------------------------------

вершина_g3(v1).
вершина_g3(v2).
вершина_g3(v3).
вершина_g3(v4).
вершина_g3(v5).

ребро_g3(v1, v2).  ребро_g3(v2, v1).
ребро_g3(v2, v3).  ребро_g3(v3, v2).
ребро_g3(v3, v4).  ребро_g3(v4, v3).
ребро_g3(v4, v5).  ребро_g3(v5, v4).

правило гам_шлях_g3(?a, ?b, ?c, ?d, ?e) якщо вершина_g3(?a) і вершина_g3(?b) і ?a ≠ ?b і ребро_g3(?a, ?b) і вершина_g3(?c) і ?a ≠ ?c і ?b ≠ ?c і ребро_g3(?b, ?c) і вершина_g3(?d) і ?a ≠ ?d і ?b ≠ ?d і ?c ≠ ?d і ребро_g3(?c, ?d) і вершина_g3(?e) і ?a ≠ ?e і ?b ≠ ?e і ?c ≠ ?e і ?d ≠ ?e і ребро_g3(?d, ?e).

правило гам_цикл_g3(?a, ?b, ?c, ?d, ?e) якщо гам_шлях_g3(?a, ?b, ?c, ?d, ?e) і ребро_g3(?e, ?a).


% ============================================================
% Запити
% ============================================================

% Граф 1: квадрат — очікувано 8 гамільтонових шляхів
?- гам_шлях_g1(?a, ?b, ?c, ?d).

% Граф 1: всі шляхи також утворюють цикли (8 циклів)
?- гам_цикл_g1(?a, ?b, ?c, ?d).

% Граф 1: конкретна перевірка
?- гам_шлях_g1(v1, v2, v3, v4).

% Граф 2: K4 — очікувано 24 шляхи (усі перестановки 4 вершин)
?- гам_шлях_g2(?a, ?b, ?c, ?d).

% Граф 2: 24 цикли (бо K4 повний, ребро від останньої до першої завжди є)
?- гам_цикл_g2(?a, ?b, ?c, ?d).

% Граф 3: лінійний — рівно 2 шляхи
?- гам_шлях_g3(?a, ?b, ?c, ?d, ?e).

% Граф 3: жодного циклу, бо v1 і v5 не з'єднані
?- гам_цикл_g3(?a, ?b, ?c, ?d, ?e).

% Traced запит: показати як movemok шукає шляхи у квадраті
?- слід гам_шлях_g1(?a, ?b, ?c, ?d).


% ============================================================
% Примітка про складність
% ============================================================
%
% Без incremental constraint satisfaction (обмеження перевіряються
% в кінці правила) movemok генерує повну декартову таблицю
% N^K комбінацій, де N — кількість вершин, K — арність шляху.
% Для K4: 4^4 = 256 комбінацій до фільтрування.
%
% З incremental constraint satisfaction (як у цьому файлі)
% backtracking обрізає гілки одразу:
%   - вибір b обмежений сусідами a → ~2-3 варіанти
%   - вибір c обмежений сусідами b і не в {a} → ~1-2 варіанти
%   - вибір d обмежений сусідами c і не в {a,b} → 0-1 варіант
%
% Це різниця між експоненційним повним перебором і майже
% лінійним DFS по графу. Класичний урок логічного програмування:
% **порядок цілей у тілі правила має значення**.
%
% Для графів більше ~10 вершин навіть оптимізований backtracking
% стає повільним. Рішення: ASP (Clingo має вбудовану підтримку
% circuit/hamilton через extensions), CLP(FD) у SWI-Prolog,
% або спеціалізовані TSP/Hamiltonian solvers на C++.
% ============================================================
