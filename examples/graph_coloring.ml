% ============================================================
% graph_coloring.ml — розфарбування графа через generate-and-test
% ============================================================
%
% Класична задача теорії графів: призначити кожній вершині колір
% так, щоб жодні дві сусідні вершини не мали однакового кольору.
%
% Цей приклад показує ідіому generate-and-test — одну з найстаріших
% технік логічного програмування. Movемок пробує всі можливі
% комбінації кольорів і backtrack-ить ті, що порушують обмеження.
%
% Важливо: у minilog оператори = і ≠ працюють ТІЛЬКИ з числами.
% Тому "різність кольорів" ми виражаємо не через ?c1 ≠ ?c2,
% а через факти різні_кольори/2, які перераховують усі валідні пари.
% Це стандартний прологівський патерн для атомів.
%
% Граф 1: трикутник K3 (три вершини, три ребра, три кольори).
% Граф 2: K4 мінус одне ребро (чотири вершини, п'ять ребер).
%
% Для великих графів цей підхід експоненційний і непридатний.
% Для реального розфарбування графів використовують ASP (Clingo)
% або CLP(FD) у SWI-Prolog. minilog тут — для навчання ідіоми.
% ============================================================


% ------------------------------------------------------------
% Кольори і відношення "різні кольори"
% ------------------------------------------------------------

% Три доступних кольори
колір(червоний).
колір(зелений).
колір(синій).

% Усі пари різних кольорів (6 фактів для 3 кольорів)
% Це наша база для перевірки "колір X ≠ колір Y" на атомах
різні_кольори(червоний, зелений).
різні_кольори(червоний, синій).
різні_кольори(зелений, червоний).
різні_кольори(зелений, синій).
різні_кольори(синій, червоний).
різні_кольори(синій, зелений).


% ============================================================
% Граф 1: трикутник K3
% ============================================================
%
%     a
%    / \
%   b---c
%
% Три вершини: a, b, c. Усі три попарно сусідні.
% Хроматичне число: 3 (кожна вершина має мати свій колір).
% ------------------------------------------------------------

% Ребра графа K3 (неорієнтовані — задаємо один раз)
ребро_k3(a, b).
ребро_k3(b, c).
ребро_k3(a, c).

% Generate-and-test: призначити колір кожній з трьох вершин,
% перевірити, що сусідні вершини мають різні кольори
правило розфарбування_k3(?ca, ?cb, ?cc) якщо колір(?ca) і колір(?cb) і колір(?cc) і різні_кольори(?ca, ?cb) і різні_кольори(?cb, ?cc) і різні_кольори(?ca, ?cc).


% ============================================================
% Граф 2: K4 мінус одне ребро
% ============================================================
%
%     a---b
%     |\ /|
%     | X |
%     |/ \|
%     d---c      (без ребра a-c)
%
% Чотири вершини, п'ять ребер: a-b, a-d, b-c, b-d, c-d.
% Пара (a, c) НЕ сусідня — отже, a і c можуть мати однаковий колір.
% Хроматичне число: 3.
% ------------------------------------------------------------

% Ребра графа
ребро_g2(a, b).
ребро_g2(a, d).
ребро_g2(b, c).
ребро_g2(b, d).
ребро_g2(c, d).

% Розфарбування: перевіряємо всі 5 ребер
правило розфарбування_g2(?ca, ?cb, ?cc, ?cd) якщо колір(?ca) і колір(?cb) і колір(?cc) і колір(?cd) і різні_кольори(?ca, ?cb) і різні_кольори(?ca, ?cd) і різні_кольори(?cb, ?cc) і різні_кольори(?cb, ?cd) і різні_кольори(?cc, ?cd).


% ============================================================
% Запити
% ============================================================

% Перевірка конкретних кольорів для K3
% (червоний, зелений, синій) має працювати
?- розфарбування_k3(червоний, зелений, синій).

% Всі можливі валідні розфарбування K3
% Має бути 6 розв'язків (3! перестановок трьох кольорів)
?- розфарбування_k3(?a, ?b, ?c).

% Пошук валідного розфарбування K4 мінус ребро
% Має знайти кілька розв'язків, включно з a=c
?- розфарбування_g2(?a, ?b, ?c, ?d).

% Traced запит: показати як movemok шукає розфарбування K3
?- слід розфарбування_k3(?a, ?b, ?c).

% Негативний приклад: (червоний, червоний, синій) не валідне для K3,
% бо a і b мають однаковий колір, а вони сусідні
?- розфарбування_k3(червоний, червоний, синій).


% ============================================================
% Примітка про масштабованість
% ============================================================
%
% Кількість комбінацій, які movемок перебирає у найгіршому випадку:
%   K3: 3^3 = 27 комбінацій
%   G2: 3^4 = 81 комбінацій
%   Граф на 10 вершин з 3 кольорами: 3^10 = 59 049
%   Граф на 20 вершин з 3 кольорами: 3^20 ≈ 3.5 мільярда
%
% Backtracking обрізає більшість гілок рано, але у найгіршому
% випадку (щільні графи, багато кольорів) це експоненційно.
%
% Також зверніть увагу, що правила розфарбування мають
% ФІКСОВАНУ арність (3 для K3, 4 для G2). Генеричне "розфарбуй
% довільний граф" у minilog Phase 1 неможливе, бо потребує
% списків як first-class, яких minilog не має (див. "Свідомі
% обмеження" у docs/language-reference.md).
%
% Для реального розфарбування графів використовуйте:
%   - Clingo (Answer Set Programming) — 3 рядки коду, сотні
%     вершин за мілісекунди
%   - SWI-Prolog з library(clpfd) — constraint propagation
%   - Soufflé (Datalog) для дуже великих баз фактів
% ============================================================
