% ================================================================
% euclidean_geometry.ml
% Евклідова геометрія як формальна система: від аксіом до теорем
% ================================================================
%
% Цей приклад демонструє, як minilog виражає Евклідову аксіоматичну
% побудову геометрії. Мета — не класифікувати трикутники (це робить
% examples/geometry_triangles.ml), а показати СТРУКТУРУ формальної
% системи: як з мінімального набору аксіом і визначень виводяться
% теореми через proof trees.
%
% Читайте разом з docs/euclidean-geometry-from-axioms.md, де повністю
% розкрито історичний контекст, постулати Евкліда словами, і класичні
% доведення Proposition 1, 5, 47.
%
% Структура файлу:
%   Розділ 1 — П'ять постулатів як meta-факти
%   Розділ 2 — П'ять загальних понять (аксіоми рівності)
%   Розділ 3 — Координатна модель: точки і відрізки
%   Розділ 4 — Визначення через правила
%   Розділ 5 — Теореми як правила
%   Розділ 6 — Запити: теореми як доведення
% ================================================================


% ================================================================
% Розділ 1. Постулати Евкліда як meta-факти
% ================================================================
%
% Евклід постулював п'ять тверджень, які приймаються без доведення.
% У minilog ми зберігаємо їх як факти на meta-рівні — вони не беруть
% участі в обчисленні теорем, але документують, що саме приймається
% за аксіому.
%
% minilog не може виконати постулати 1, 2, 3 — вони описують
% конструктивні операції (провести пряму, продовжити, описати коло),
% яких у нас немає. Постулати 4 і 5 виражаються через координатну
% модель. Це фундаментальне обмеження: Евклід має конструктивну
% геометрію з циркулем і лінійкою, minilog — обчислювальну з
% координатами.

факт постулат(1, "Від будь-якої точки до будь-якої точки можна провести пряму").
факт постулат(2, "Обмежену пряму можна неперервно продовжити у пряму").
факт постулат(3, "З будь-якого центру і радіусом можна описати коло").
факт постулат(4, "Усі прямі кути рівні між собою").
факт постулат(5, "Якщо пряма перетинає дві так, що сума внутрішніх кутів з одного боку менша двох прямих, то ці прямі перетнуться з того боку").


% ================================================================
% Розділ 2. Загальні поняття (common notions) — аксіоми рівності
% ================================================================
%
% Евклід виділив п'ять "загальних понять" — базових законів
% рівності, які ми сьогодні назвали б аксіомами теорії рівності.
% У minilog вони вже вбудовані у механізм уніфікації: дві змінні
% уніфікуються, якщо їхні значення збігаються, і ця рівність
% автоматично транзитивна.
%
% CN4 ("речі, що збігаються, рівні між собою") — це буквально
% уніфікація minilog. Коли правило використовує одну змінну ?l
% у двох місцях, minilog гарантує, що обидва значення збігнуться.

факт аксіома(cn1, "Речі, рівні одній і тій самій речі, рівні між собою").
факт аксіома(cn2, "Якщо до рівних додати рівні, то цілі будуть рівні").
факт аксіома(cn3, "Якщо від рівних відняти рівні, то залишки будуть рівні").
факт аксіома(cn4, "Речі, що збігаються одна з одною, рівні між собою").
факт аксіома(cn5, "Ціле більше від частини").


% ================================================================
% Розділ 3. Координатна модель
% ================================================================
%
% Щоб працювати з Евклідовими об'єктами обчислювально, вводимо
% Декартову модель: точки мають координати у R², відрізки задаються
% парами точок. Це Гільбертова модель (1899) на відміну від
% синтетичної геометрії Евкліда.
%
% Трикутник T1 — рівнобедрений 5-5-6 з пифагорової трійки (3,4,5):
%   A = (0, 0), B = (6, 0), C = (3, 4)
%   |AB| = 6, |AC| = 5, |BC| = 5
% Вибір пифагорової трійки забезпечує, що всі довжини — цілі
% числа без проблем з плаваючою комою.
%
% Трикутник T2 — прямокутний 3-4-5:
%   D = (0, 0), E = (4, 0), F = (0, 3)
%   |DE| = 4, |DF| = 3, |EF| = 5, прямий кут у D.

точка(a, 0, 0).
точка(b, 6, 0).
точка(c, 3, 4).

точка(d, 0, 0).
точка(e, 4, 0).
точка(f, 0, 3).

відрізок(ab, a, b).
відрізок(bc, b, c).
відрізок(ac, a, c).
відрізок(de, d, e).
відрізок(ef, e, f).
відрізок(df, d, f).

трикутник(t1, a, b, c).
трикутник(t2, d, e, f).

% Кути задаються як факти, бо minilog не має тригонометрії (sin, cos,
% atan2). Для t1 кути при основі AB (біля A і B) рівні — це наслідок
% Pons Asinorum (Proposition 5), який ми перевіримо правилом.
кут(t1, a, 53).
кут(t1, b, 53).
кут(t1, c, 74).

кут(t2, d, 90).
кут(t2, e, 37).
кут(t2, f, 53).


% ================================================================
% Розділ 4. Визначення через правила
% ================================================================

% Визначення 1: довжина відрізка (метрика Декарта)
% Відповідає Евклідовому поняттю "величина відрізка", але
% реалізується через обчислення, не через порівняння з еталоном.
правило довжина(?s, ?l) якщо
    відрізок(?s, ?p1, ?p2) і
    точка(?p1, ?x1, ?y1) і
    точка(?p2, ?x2, ?y2) і
    ?l = sqrt((?x2 - ?x1) * (?x2 - ?x1) + (?y2 - ?y1) * (?y2 - ?y1)).

% Визначення 2: конгруентність відрізків
% Це буквальне застосування CN4: два відрізки конгруентні, якщо
% їхні довжини уніфікуються через одну й ту саму змінну ?l.
% minilog тут реалізує загальне поняття Евкліда автоматично.
правило конгруентні(?s1, ?s2) якщо
    довжина(?s1, ?l) і
    довжина(?s2, ?l).

% Визначення 3: сторони трикутника
% Трикутник ABC має три сторони: AB, BC, CA.
правило сторона(?t, ?s) якщо
    трикутник(?t, ?p1, ?p2, ?_) і
    відрізок(?s, ?p1, ?p2).
правило сторона(?t, ?s) якщо
    трикутник(?t, ?_, ?p2, ?p3) і
    відрізок(?s, ?p2, ?p3).
правило сторона(?t, ?s) якщо
    трикутник(?t, ?p1, ?_, ?p3) і
    відрізок(?s, ?p1, ?p3).


% ================================================================
% Розділ 5. Теореми як правила
% ================================================================

% Теорема (Proposition 5 Книги I — Pons Asinorum, "міст ослів")
% "У рівнобедреному трикутнику кути при основі рівні"
%
% Евклідове доведення словами використовує конструкцію допоміжних
% трикутників і SAS-конгруентність. У minilog ми верифікуємо
% наслідок: якщо дві сторони конгруентні (через CN4), то кути при
% основі (задані як факти) мають бути рівні.
%
% Це НЕ доведення Proposition 5 у сенсі Евкліда. Це перевірка, що
% для заданої конфігурації висновок теореми виконується. Повне
% синтетичне доведення неможливе у minilog Phase 1.
правило pons_asinorum(?t) якщо
    трикутник(?t, ?p1, ?p2, ?p3) і
    відрізок(?left, ?p1, ?p3) і
    відрізок(?right, ?p2, ?p3) і
    конгруентні(?left, ?right) і
    кут(?t, ?p1, ?alpha) і
    кут(?t, ?p2, ?alpha).

% Теорема (Proposition 20 Книги I — нерівність трикутника)
% "У будь-якому трикутнику сума двох сторін більша за третю"
%
% Це властивість, яка виконується для будь-якого невиродженого
% трикутника на Декартовій площині. Доведення через обчислення
% довжин і застосування порядкових відношень.
правило нерівність_трикутника(?t) якщо
    трикутник(?t, ?p1, ?p2, ?p3) і
    відрізок(?s1, ?p1, ?p2) і
    відрізок(?s2, ?p2, ?p3) і
    відрізок(?s3, ?p1, ?p3) і
    довжина(?s1, ?a) і
    довжина(?s2, ?b) і
    довжина(?s3, ?c) і
    ?a + ?b > ?c і
    ?b + ?c > ?a і
    ?a + ?c > ?b.

% Теорема (Proposition 47 Книги I — теорема Піфагора)
% "У прямокутних трикутниках квадрат на стороні, що лежить проти
% прямого кута, дорівнює сумі квадратів на сторонах, які утворюють
% прямий кут"
%
% Три правила покривають три можливих розташування гіпотенузи
% (будь-яка з трьох сторін може виявитися найдовшою, якщо ми не
% знаємо заздалегідь).
правило піфагор(?t) якщо
    трикутник(?t, ?p1, ?p2, ?p3) і
    відрізок(?s1, ?p1, ?p2) і
    відрізок(?s2, ?p2, ?p3) і
    відрізок(?s3, ?p1, ?p3) і
    довжина(?s1, ?a) і довжина(?s2, ?b) і довжина(?s3, ?c) і
    ?a * ?a + ?b * ?b = ?c * ?c.
правило піфагор(?t) якщо
    трикутник(?t, ?p1, ?p2, ?p3) і
    відрізок(?s1, ?p1, ?p2) і
    відрізок(?s2, ?p2, ?p3) і
    відрізок(?s3, ?p1, ?p3) і
    довжина(?s1, ?a) і довжина(?s2, ?b) і довжина(?s3, ?c) і
    ?a * ?a + ?c * ?c = ?b * ?b.
правило піфагор(?t) якщо
    трикутник(?t, ?p1, ?p2, ?p3) і
    відрізок(?s1, ?p1, ?p2) і
    відрізок(?s2, ?p2, ?p3) і
    відрізок(?s3, ?p1, ?p3) і
    довжина(?s1, ?a) і довжина(?s2, ?b) і довжина(?s3, ?c) і
    ?b * ?b + ?c * ?c = ?a * ?a.


% ================================================================
% Розділ 6. Запити — теореми як доведення
